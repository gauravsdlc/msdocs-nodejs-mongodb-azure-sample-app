// router.post('/logout', validate(authValidation.logout), authController.logout);
//authorize
router.route('/check')
    .post(authApiKey(), authController.authApi);
//give user and orgDetails
router.route('/authorize')
    .get(authApiKey(), authController.getAuthorizedDetails);
module.exports = router;

