const express = require('express');
const validate = require('../../middlewares/validate');
const authValidation = require('../../validations/auth.validation');
const authController = require('../../controllers/auth.controller');
const auth = require('../../middlewares/auth');
const authApiKey = require('../../middlewares/authApiKey');

const router = express.Router();

router.post('/login-with-email', validate(authValidation.adminLoginWithEmail), authController.adminLoginWithEmail);
router.post('/login-with-membershipId', validate(authValidation.adminLoginWithMembershipId), authController.adminLoginWithMembershipId);
router.post('/getCurrentUser', authController.getCurrentUser);
router.post('/forgot-password', validate(authValidation.forgotPasswordValidation),authController.forgotPassword);
router.post('/reset-password', validate(authValidation.resetPasswordValidation),authController.resetPassword);
router.post('/change-password', auth('manageOrg'),validate(authValidation.changePasswordValidation),authController.changePassword);

